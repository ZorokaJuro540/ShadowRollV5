def get_fallback_image(character_name: str) -> str:
    """Get fallback image URL for a character"""
    return FALLBACK_IMAGES.get(character_name)

def get_character_suggestions(anime_name: str) -> list:
    """Get character suggestions for an anime"""
    suggestions = []
    anime_lower = anime_name.lower()
    
    for char_name in FALLBACK_IMAGES.keys():
        # Simple matching logic - can be improved
        if any(word in char_name.lower() for word in anime_lower.split()):
            suggestions.append(char_name)
    
    return suggestions